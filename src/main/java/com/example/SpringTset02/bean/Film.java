package com.example.SpringTset02.bean;

public class Film {
    private Integer film_id;
    private String film_name;
    private String film_time;
    private String film_abstract;
    private String film_actors;
    private String film_evaluate;
    private String film_will;
    private String film_pic;

    public String getFilm_will() {
        return film_will;
    }

    public void setFilm_will(String film_will) {
        this.film_will = film_will;
    }

    public Integer getFilm_id() {
        return film_id;
    }

    public void setFilm_id(Integer film_id) {
        this.film_id = film_id;
    }

    public String getFilm_name() {
        return film_name;
    }

    public void setFilm_name(String film_name) {
        this.film_name = film_name;
    }

    public String getFilm_time() {
        return film_time;
    }

    public void setFilm_time(String film_time) {
        this.film_time = film_time;
    }

    public String getFilm_abstract() {
        return film_abstract;
    }

    public void setFilm_abstract(String film_abstract) {
        this.film_abstract = film_abstract;
    }

    public String getFilm_actors() {
        return film_actors;
    }

    public void setFilm_actors(String film_actors) {
        this.film_actors = film_actors;
    }

    public String getFilm_evaluate() {
        return film_evaluate;
    }

    public void setFilm_evaluate(String film_evaluate) {
        this.film_evaluate = film_evaluate;
    }

    public String getFilm_pic() {
        return film_pic;
    }

    public void setFilm_pic(String film_pic) {
        this.film_pic = film_pic;
    }

}
